package com.loadsheet.bean.data.CSheet;

public class StabTrim {
	private int 			ahmRevId;
	private int 			stabTrimId;
	private double		max;
	private double		min;
	private String 		name;
	private String 		status;
	
	public int getAhmRevId() {
		return ahmRevId;
	}
	public void setAhmRevId(int ahmRevId) {
		this.ahmRevId = ahmRevId;
	}
	public int getStabTrimId() {
		return stabTrimId;
	}
	public void setStabTrimId(int stabTrimId) {
		this.stabTrimId = stabTrimId;
	}
	public double getMax() {
		return max;
	}
	public void setMax(double ma) {
		this.max = ma;
	}
	public double getMin() {
		return min;
	}
	public void setMin(double min) {
		this.min = min;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	
	
	
}
