package com.loadsheet.bean.data.CSheet;

public class Ballast {
	private int 			ahmRevId;
	private int 			ballastId;
	private String 		ballastLocation;
	private double 	weight;
	private double 	ballastIndex;
	private String		operate;
	public int getAhmRevId() {
		return ahmRevId;
	}
	public void setAhmRevId(int ahmRevId) {
		this.ahmRevId = ahmRevId;
	}
	public int getBallastId() {
		return ballastId;
	}
	public void setBallastId(int ballastId) {
		this.ballastId = ballastId;
	}
	public String getBallastLocation() {
		return ballastLocation;
	}
	public void setBallastLocation(String ballastLocation) {
		this.ballastLocation = ballastLocation;
	}
	public double getWeight() {
		return weight;
	}
	public void setWeight(double weight) {
		this.weight = weight;
	}
	public double getBallastIndex() {
		return ballastIndex;
	}
	public void setBallastIndex(double ballastIndex) {
		this.ballastIndex = ballastIndex;
	}
	public String getOperate() {
		return operate;
	}
	public void setOperate(String operate) {
		this.operate = operate;
	}	
	
	
}
