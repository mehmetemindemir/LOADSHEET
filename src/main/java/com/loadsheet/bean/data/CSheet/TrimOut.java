package com.loadsheet.bean.data.CSheet;

public class TrimOut {
	private String code;
	private int outTrim;
	private String status;
	private String remarks;
	private int  ahmRevId;
	
	public String getCode() {
		return code;
	}
	public void setCode(String code) {
		this.code = code;
	}
	public int getOutTrim() {
		return outTrim;
	}
	public void setOutTrim(int outTrim) {
		this.outTrim = outTrim;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	public String getRemarks() {
		return remarks;
	}
	public void setRemarks(String remarks) {
		this.remarks = remarks;
	}
	public int getAhmRevId() {
		return ahmRevId;
	}
	public void setAhmRevId(int ahmRevId) {
		this.ahmRevId = ahmRevId;
	}
	
}
