package com.loadsheet.bean.data.CSheet;

public class BalanceOut {
	private int ahmRevId;
	private String code;
	private int outBalance;
	private String status;
	private String remarks;
	public int getAhmRevId() {
		return ahmRevId;
	}
	public void setAhmRevId(int ahmRevId) {
		this.ahmRevId = ahmRevId;
	}
	public String getCode() {
		return code;
	}
	public void setCode(String code) {
		this.code = code;
	}
	public int getOutBalance() {
		return outBalance;
	}
	public void setOutBalance(int outBalance) {
		this.outBalance = outBalance;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	public String getRemarks() {
		return remarks;
	}
	public void setRemarks(String remarks) {
		this.remarks = remarks;
	}
	
	
}
