package com.loadsheet.bean.data.CSheet;

public class EffectFuel {
	private int ahmRevId;
	private double weight;
	private double fuelIndex;
	private String  opera;
	private String status;
	private int efID;
	public int getAhmRevId() {
		return ahmRevId;
	}
	public void setAhmRevId(int ahmRevId) {
		this.ahmRevId = ahmRevId;
	}
	public double getWeight() {
		return weight;
	}
	public void setWeight(double weight) {
		this.weight = weight;
	}
	public double getFuelIndex() {
		return fuelIndex;
	}
	public void setFuelIndex(double fuelIndex) {
		this.fuelIndex = fuelIndex;
	}
	public String getOpera() {
		return opera;
	}
	public void setOpera(String opera) {
		this.opera = opera;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	public int getEfID() {
		return efID;
	}
	public void setEfID(int efID) {
		this.efID = efID;
	}
		
		
}
