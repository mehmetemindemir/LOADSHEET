package com.loadsheet.bean.data.CSheet;

public class Pantry {
	private String 		pantry;
	private double 	weight;
	private String 		operate;
	private int 			pantryId;
	private int 			ahmRevId;
	private double 	perWeight;
	private String 		ferryFlight;
	
	public String getPantry() {
		return pantry;
	}
	public void setPantry(String pantry) {
		this.pantry = pantry;
	}
	public double getWeight() {
		return weight;
	}
	public void setWeight(double weight) {
		this.weight = weight;
	}
	public String getOperate() {
		return operate;
	}
	public void setOperate(String operate) {
		this.operate = operate;
	}
	public int getPantryId() {
		return pantryId;
	}
	public void setPantryId(int pantryId) {
		this.pantryId = pantryId;
	}
	public int getAhmRevId() {
		return ahmRevId;
	}
	public void setAhmRevId(int ahmRevId) {
		this.ahmRevId = ahmRevId;
	}
	public double getPerWeight() {
		return perWeight;
	}
	public void setPerWeight(double perWeight) {
		this.perWeight = perWeight;
	}
	public String getFerryFlight() {
		return ferryFlight;
	}
	public void setFerryFlight(String ferryFlight) {
		this.ferryFlight = ferryFlight;
	}
	
	
}
