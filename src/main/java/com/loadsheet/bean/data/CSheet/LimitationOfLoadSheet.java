package com.loadsheet.bean.data.CSheet;

public class LimitationOfLoadSheet {
	private int ahmRevId;
	private String limitCode;
	private double weight;
	private double limitIndex;
	private String  opera;
	private String status;
	private int llpID;
	
	public int getAhmRevId() {
		return ahmRevId;
	}
	public void setAhmRevId(int ahmRevId) {
		this.ahmRevId = ahmRevId;
	}
	public String getLimitCode() {
		return limitCode;
	}
	public void setLimitCode(String limitCode) {
		this.limitCode = limitCode;
	}
	public double getWeight() {
		return weight;
	}
	public void setWeight(double weight) {
		this.weight = weight;
	}
	public double getLimitIndex() {
		return limitIndex;
	}
	public void setLimitIndex(double limitIndex) {
		this.limitIndex = limitIndex;
	}
	public String getOpera() {
		return opera;
	}
	public void setOpera(String opera) {
		this.opera = opera;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	public int getLlpID() {
		return llpID;
	}
	public void setLlpID(int llpID) {
		this.llpID = llpID;
	}
	
	
}
