package com.loadsheet.bean.data.DSheet;

public class CGLimits {
	private int ahmRevId;
	private String operate;
	private int weight;
	private double index;
	private int cqLimitId;
	public int getAhmRevId() {
		return ahmRevId;
	}
	public void setAhmRevId(int ahmRevId) {
		this.ahmRevId = ahmRevId;
	}
	public String getOperate() {
		return operate;
	}
	public void setOperate(String operate) {
		this.operate = operate;
	}
	public int getWeight() {
		return weight;
	}
	public void setWeight(int weight) {
		this.weight = weight;
	}
	public double getIndex() {
		return index;
	}
	public void setIndex(double index) {
		this.index = index;
	}
	public int getCqLimitId() {
		return cqLimitId;
	}
	public void setCqLimitId(int cqLimitId) {
		this.cqLimitId = cqLimitId;
	}
	
	
}
