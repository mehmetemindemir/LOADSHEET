public class CarrierData {
	//airline_name,iata_designator,three_digit_code,icao_designator
	private String airlineName;
	private String iataDes;
	private String iataCode;
	private String icaoDes;
	public String getAirlineName() {
		return airlineName;
	}
	public void setAirlineName(String airlineName) {
		this.airlineName = airlineName;
	}
	public String getIataDes() {
		return iataDes;
	}
	public void setIataDes(String iataDes) {
		this.iataDes = iataDes;
	}
	public String getIataCode() {
		return iataCode;
	}
	public void setIataCode(String iataCode) {
		this.iataCode = iataCode;
	}
	public String getIcaoDes() {
		return icaoDes;
	}
	public void setIcaoDes(String icaoDes) {
		this.icaoDes = icaoDes;
	}
	
}
