package com.loadsheet.bean.data.company;

public class CompanyProcessCount {
	private String date;
	private String totalChecked;
	private String company;
	public String getDate() {
		return date;
	}
	public void setDate(String date) {
		this.date = date;
	}
	public String getTotalChecked() {
		return totalChecked;
	}
	public void setTotalChecked(String totalChecked) {
		this.totalChecked = totalChecked;
	}
	public String getCompany() {
		return company;
	}
	public void setCompany(String company) {
		this.company = company;
	}
}
